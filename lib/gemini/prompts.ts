/**
 * サービスヒントを含むプロンプトを生成
 * @param serviceHint 決済サービスのヒント
 * @returns プロンプト文字列
 */
export function buildServiceHintPrompt(
  serviceHint?: PaymentService
): string {
  if (!serviceHint || serviceHint === "unknown") {
    return "";
  }
  return `\n決済サービスのヒント: ${getPaymentServiceName(serviceHint)}`;
}

